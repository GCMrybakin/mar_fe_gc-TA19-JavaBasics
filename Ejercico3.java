
public class Ejercico3 {

	public static void main(String[] args) {
		
		int X = 5;
        int Y = 10;
        double M = 5.5;
        double N = 2.3;
        
        System.out.println("El valor de cada variable.");
        System.out.println("Variable X = "+X);
        System.out.println("Variable Y = "+Y);
        System.out.println("Variable M = "+M);
        System.out.println("Variable N = "+N);
        
        System.out.println("La suma de X + Y.");
        System.out.println(X + " + " +Y+ " = "+(X+Y));
        System.out.println("La resta de X - Y.");
        System.out.println(X + " - " +Y+ " = "+(X-Y));
        System.out.println("El producto de X * Y.");
        System.out.println(X + " * " +Y+ " = "+X*Y);
        System.out.println("El cociente de X / Y.");
        System.out.println(X + " / " +Y+ " = "+X/Y);
        System.out.println("El resto de X % Y.");
        System.out.println(X + " % " +Y+ " = "+X%Y);
        System.out.println("La suma de N + M.");
        System.out.println(N + " + " +M+ " = "+(N+M));
        System.out.println("La resta de N - M.");
        System.out.println(N + " - " +M+ " = "+(N-M));
        System.out.println("El producto de N * M.");
        System.out.println(N + " * " +M+ " = "+N*M);
        System.out.println("El cociente de N / M.");
        System.out.println(N + " / " +M+ " = "+N/M);
        System.out.println("El resto de N % M.");
        System.out.println(N + " % " +M+ " = "+N%M);
        System.out.println("La suma de X + N.");
        System.out.println(X + " + " +N+ " = "+(X+N));
        System.out.println("El cociente de Y + M.");
        System.out.println(Y + " / " +M+ " = "+Y/M);
        System.out.println("El resto de Y % M.");
        System.out.println(Y + " % " +M+ " = "+Y%M);
        
        System.out.println("El doble de cada variable.");
        System.out.println("X = "+X+" doble "+2*X);
        System.out.println("Y = "+Y+" doble "+2*Y);
        System.out.println("M = "+M+" doble "+2*M);
        System.out.println("N = "+N+" doble "+2*N);
        
        System.out.println("La suma de todas las variables.");
        System.out.println(X+" + "+Y+" + "+N+" + "+M+" = "+(X+Y+M+N));
        System.out.println("El producto de todas las variables.");
        System.out.println(X+" * "+Y+" * "+N+" * "+M+" = "+(X*Y*M*N));

	}

}
